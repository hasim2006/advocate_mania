// Mock window.scrollTo
window.scrollTo = vi.fn();

// Mock window.dispatchEvent
const originalDispatchEvent = window.dispatchEvent.bind(window);
window.dispatchEvent = vi.fn((event) => originalDispatchEvent(event));
